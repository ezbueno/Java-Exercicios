package aulas;

public class Aula_14 {
    
    public static void main (String[] args){
        
        int x = 10;
        
        x = x + 5;
        x += 5;
        
        x = x - 5;
        x -= 5;
        
        x = x * 5;
        x *= 5;
        
        x = x / 5;
        x /= 5;
        
        x++;
        ++x;
        
        x--;
        --x;
        
        System.out.println(x);
        
    }
    
}
